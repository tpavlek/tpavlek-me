    <h1 class="center">The Missing Middle</h1>

    <article class="is-centered post">
        <div class="post-content">
            <p>
                Infill is a passionate issue for the residents of Ward 11 -- there are strong perspectives on both sides.
                Many recognize the need to revitalize our neighbourhoods with new families, others legitimately complain about
                construction issues and radical changes to the character of a neighbourhood, when small bungalows are torn
                down for monster <a href="http://99percentinvisible.org/episode/mcmansion-hell-devil-details/">McMansions</a>.
                There is a lot of anti-infill sentiment pervasive in our city. But that's because we haven't shown our city what
                infill <em>could be</em>.
            </p>

            <p>
                Enter the missing middle. Don Iveson recently wrote a <a href="https://doniveson.ca/2017/08/31/building-missing-middle/">post</a>
                about the missing middle that was quite apt, and I strongly support Mayor Iveson's leadership on this file
                going forward.
            </p>

            <div class="center">
                <blockquote class="twitter-tweet" data-conversation="none" data-lang="en"><p lang="en" dir="ltr">There are &#39;wedge&#39; cities &amp; there are &#39;donut&#39; cities. Thanks to <a href="https://twitter.com/hashtag/MNO?src=hash">#MNO</a>, <a href="https://twitter.com/hashtag/yeg?src=hash">#yeg</a> is choking on that donut. <a href="https://t.co/pjJPrFM3ZU">pic.twitter.com/pjJPrFM3ZU</a></p>&mdash; Louis Pereira (@thirdstone_inc) <a href="https://twitter.com/thirdstone_inc/status/843972464845709313">March 20, 2017</a></blockquote>
                <script async src="//platform.twitter.com/widgets.js" charset="utf-8"></script>
            </div>

            <p>
                When I knock doors in mature neighbourhoods like Hazeldean and Ritchie, many of the old postwar bungalows have a basement
                suite already. If we tear one of those down and build a massive duplex on the lot, we <em>haven't actually increased density</em>
                but we've aggravated neighbours in the community with construction, variance concerns, light blocking and a whole host of other issues,
                all the while increasing the cost for new, young families to enter the neighbourhood.
            </p>

            <p>
                Let's get out of the thinking where "infill" is tearing down and replacing lot-for-lot. Let's talk about
                stacked row-housing, courtyard housing and low-rise apartments along our transit and active transportation corridors.
            </p>

            <blockquote>
                <p>
                    Let's talk about a better way to do infill.
                </p>
            </blockquote>

